      public void listarCargo()
    {
        modeloCargo.clear();
        try {
            List<Cargo> listCargo = objCargoCOM.listarTodosOrdenado();
            if(listCargo!=null)
            {
                for(Cargo m : listCargo)
                {
                    modeloCargo.addElement(m);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,e.getMessage());
        }
    }
