private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
     if(!lblIdCargo.getText().equals("Autogenerado"))
        {
            try {
            if(objCargoCOM.eliminar(Integer.parseInt(lblIdCargo.getText())))
            {
                JOptionPane.showMessageDialog(this,"Se Elimino Correctamemte");
                listarCargo();
            }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this,e.getMessage());
            }            
        }
        else
            JOptionPane.showMessageDialog(this,"Seleccione una Cargo");
        limpiarControles();
        
}//GEN-LAST:event_btnEliminarActionPerformed

private void jListaCargoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jListaCargoMouseClicked

       String criterio = jListaCargo.getSelectedValue().toString();
       if(!criterio.toString().trim().equals(""))
       {
                
                txtNombre.setText(((Cargo)jListaCargo.getModel().getElementAt(jListaCargo.getSelectedIndex())).getStr_nombre());
                txtDescripcion.setText(((Cargo)jListaCargo.getModel().getElementAt(jListaCargo.getSelectedIndex())).getStr_descripcion());
                lblIdCargo.setText(String.valueOf(((Cargo)jListaCargo.getModel().getElementAt(jListaCargo.getSelectedIndex())).getInt_id_cargo()));
                cboEstado.setSelectedIndex((((Cargo)jListaCargo.getModel().getElementAt(jListaCargo.getSelectedIndex())).getIntl_activa()));

       }
    
}//GEN-LAST:event_jListaCargoMouseClicked

private void txtBuscarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBuscarKeyReleased
// TODO add your handling code here:
      modeloCargo.clear();
        try {
            List<Cargo> listCargo = objCargoCOM.listarFiltro(txtBuscar.getText());
            if(listCargo!=null)
            {
                for(Cargo m : listCargo)
                {
                    modeloCargo.addElement(m);
                }
            }
         
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,e.getMessage());
        }
}//GEN-LAST:event_txtBuscarKeyReleased

