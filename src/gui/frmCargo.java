private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed
       
    if(lblIdCargo.getText().equals("Autogenerado"))
    {
     if(txtNombre.getText() == null ? "" != null : !txtNombre.getText().equals("") )

        try {
            Cargo objCargo= new Cargo();
            
            objCargo.setIntl_activa(cboEstado.getSelectedIndex());

            objCargo.setStr_nombre(txtNombre.getText());

             
            objCargo.setStr_descripcion(txtDescripcion.getText());
            
            
            if(objCargoCOM.insertar(objCargo))
            {
                JOptionPane.showMessageDialog(this,"Se Registro Correctamemte");
                listarCargo();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,"Nombre Duplicado");
     }
          else
            JOptionPane.showMessageDialog(this,"Ingrese datos");
    }
        limpiarControles();
    
}//GEN-LAST:event_btnGrabarActionPerformed

private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed

    if(!lblIdCargo.getText().equals("Autogenerado"))
      try {
              Cargo objCargo= new Cargo();
             
            objCargo.setIntl_activa(cboEstado.getSelectedIndex());

            objCargo.setStr_nombre(txtNombre.getText());
            
            objCargo.setStr_descripcion(txtDescripcion.getText());
           
            objCargo.setInt_id_cargo(Integer.parseInt(lblIdCargo.getText()));
            
            if(objCargoCOM.actualizar(objCargo))
            {
                JOptionPane.showMessageDialog(this,"Se Actualizo Correctamente");
                
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,e.getMessage());
        }

    else
     JOptionPane.showMessageDialog(this,"Seleccione una Cargo");
         listarCargo();   

}//GEN-LAST:event_btnActualizarActionPerformed

