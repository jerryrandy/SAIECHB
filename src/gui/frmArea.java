private void jListaAreaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jListaAreaMouseClicked
       String Nombre="";
       String criterio = jListaArea.getSelectedValue().toString();
       if(!"".equals(criterio))
       {                
                Nombre=(((Area)jListaArea.getModel().getElementAt(jListaArea.getSelectedIndex())).getStr_nombre());
                int corte = Nombre.indexOf("_");
                txtNombre.setText(Nombre.substring(corte+1));
                txtDescripcion.setText(((Area)jListaArea.getModel().getElementAt(jListaArea.getSelectedIndex())).getStr_descripcion());
                lblIdArea.setText(String.valueOf(((Area)jListaArea.getModel().getElementAt(jListaArea.getSelectedIndex())).getInt_id_area()));
 
                   int departamento=((Area)jListaArea.getModel().getElementAt(jListaArea.getSelectedIndex())).getObjDepartamento().getInt_id_departamento();

                   for(int i=1;i<cb_ListarDepartamento.getItemCount();i++)
                   {
                       int buscarDepartamento=((Departamento)cb_ListarDepartamento.getModel().getElementAt(i)).getInt_id_departamento();
                       if(departamento==buscarDepartamento)
                             cb_ListarDepartamento.setSelectedIndex(i); 
                   }
                 
                
               cboEstado.setSelectedIndex((((Area)jListaArea.getModel().getElementAt(jListaArea.getSelectedIndex())).getIntl_activa()));
              
       }
    
}//GEN-LAST:event_jListaAreaMouseClicked

private void txtBuscarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBuscarKeyReleased
// TODO add your handling code here:
      modeloArea.clear();
        try {
            List<Area> listArea = objAreaCOM.listarFiltro(txtBuscar.getText());
            if(listArea!=null)
            {
                for(Area a : listArea)
                {
                    modeloArea.addElement(a);
                }
            }           
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,e.getMessage());
        }
}//GEN-LAST:event_txtBuscarKeyReleased

