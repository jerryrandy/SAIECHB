    private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed
           
 try {
     
     for( Monitor objMonitor: modeloMonitor.listar())
     {
          if(objCPatrimonialCOM.insertar(objMonitor.getStr_id_monitor()))
               if(objMonitorCOM.insertar(objMonitor))
               {
                   objComponenteCOM.actualizarEstado(objMonitor.getObjComponente().getStr_id_componente(),4); 
          
               }
         
     }

    
           
                     JOptionPane.showMessageDialog(this,"Se Registro Correctamemte");
        } catch (Exception ex) {
            Logger.getLogger(frmIngresoMonitor.class.getName()).log(Level.SEVERE, null, ex);
        }
 
 
     limpiarControles();
     listarComboMonitor();
    }//GEN-LAST:event_btnGrabarActionPerformed

