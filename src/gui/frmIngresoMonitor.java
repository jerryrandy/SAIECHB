    private void btnAgregarAc2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarAc2ActionPerformed
        // TODO add your handling code here:
           if(!txtIdMonitor.getText().equals("") && cb_ListarComponenteMonitor.getSelectedIndex()!=0  )
   {
       Componente objComponente = new Componente();
       objComponente.setStr_id_componente(((Componente)cb_ListarComponenteMonitor.getSelectedItem()).getStr_id_componente());
        objComponente.setInt_estado(((Componente)cb_ListarComponenteMonitor.getSelectedItem()).getInt_estado());
        objComponente.setInt_garantia(((Componente)cb_ListarComponenteMonitor.getSelectedItem()).getInt_garantia());
        objComponente.setStr_caracteristica(((Componente)cb_ListarComponenteMonitor.getSelectedItem()).getStr_caracteristica());
        objComponente.setObjModelo(((Componente)cb_ListarComponenteMonitor.getSelectedItem()).getObjModelo());

        Monitor items = new Monitor (txtIdMonitor.getText(),objComponente);

        
           
      if(!modeloMonitor.insertar(items))
                    JOptionPane.showMessageDialog(this,"Numero de Patrimonial o Serie Duplicado");
                    else
                    modeloMonitor.fireTableDataChanged();
            cb_ListarComponenteMonitor.setSelectedIndex(0);
   }
 else
             JOptionPane.showMessageDialog(this,"Ingrese un Nro. Patrimonial o Selecione un Monitor");
   txtIdMonitor.setText("");
    }//GEN-LAST:event_btnAgregarAc2ActionPerformed

