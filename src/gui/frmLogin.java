    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        // TODO add your handling code here:
         try {
            Usuario objUsuario = objUsuarioCOM.iniciarSesion(txtUsuario.getText(),txtPassword.getText());
            if(objUsuario!=null)
            {
                if(objUsuario.getInt_nivel()==1)               
                 new frmPrincipalAdmin(objUsuario).setVisible(true);
                else
                       new frmPrincipalUser(objUsuario).setVisible(true);
                 dispose();
            }
            else
            {
                JOptionPane.showMessageDialog(this,"Usuario o Clave Incorrectos");
                txtUsuario.setText("");
                txtPassword.setText("");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,e.getMessage());
        }
       
        
        
    }//GEN-LAST:event_btnIngresarActionPerformed

