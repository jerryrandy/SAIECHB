      public void cargarRegloj()
    {
         formato = new SimpleDateFormat("dd/MM/yyyy");
        tiempo = new Timer(1000, new ActionListener(){
            
            public void actionPerformed(ActionEvent evt){
                fecha = new Date();
                lblFecha.setText(formato.format(fecha));
                
                reloj = Calendar.getInstance();
                lblHora.setText(reloj.get(Calendar.HOUR) + ":" +
                        reloj.get(Calendar.MINUTE) + ":" +
                        reloj.get(Calendar.SECOND) + " " +
                        ((reloj.get(Calendar.AM_PM) == 0) ? "am": "pm")); //if am o pm
            }
        });
        tiempo.start();
    }
    

