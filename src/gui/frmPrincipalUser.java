    private void btn_CClaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_CClaveActionPerformed

        if(objUsuarioAdmin.getStr_clave().equals(txtCActual.getText()) )
        {
            if(txtNClave.getText().length()>5)
            {
            if(txtNClave.getText().equals(txtRClave.getText()))
            {
                
                    try {
                        objUsuarioAdmin.setStr_clave(txtNClave.getText());
                        if(objUsuarioCOM.actualizar(objUsuarioAdmin))
                        {
                             JOptionPane.showMessageDialog(this,"Se Cambio la Clave Correctamemte");
                            Panel.setVisible(true);
                             Pcambiar.setVisible(false);
                        }
                        else
                             JOptionPane.showMessageDialog(this,"Ocurrio un error");
                           
                    } catch (Exception ex) {}
                     
            }
            else
                 JOptionPane.showMessageDialog(this,"Su nueva Clave no coincide");
            }
            else
                JOptionPane.showMessageDialog(this,"Su clave debe ser de 6 digitos o mas");
                
        }
        else
             JOptionPane.showMessageDialog(this,"Clave Actual Incorrecta");
    }//GEN-LAST:event_btn_CClaveActionPerformed

