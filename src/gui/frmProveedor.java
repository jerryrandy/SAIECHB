private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed

    lblCheckNombre.setIcon(new javax.swing.ImageIcon(getClass().getResource("/imagenes/cancel.png")));
    lblCheckNombre.setToolTipText("El Campo Es Correcto");
    if(lblIdProveedor.getText().equals("Autogenerado"))
    {
     if(!txtNombre.getText().equals("") && !txtDireccion.getText().equals("") && !txtTelefono.getText().equals("") )    
        try {
            Proveedor objProveedor= new Proveedor();
            
            objProveedor.setIntl_activa(cboEstado.getSelectedIndex());
            objProveedor.setStr_nombre(txtNombre.getText());         
            objProveedor.setStr_telefono(txtTelefono.getText());
            objProveedor.setStr_fax(txtFax.getText());
            objProveedor.setStr_direccion(txtDireccion.getText());
            objProveedor.setStr_descripcion(txtDescripcion.getText());
            
            
            if(objProveedorCOM.insertar(objProveedor)!=0)
            {
                JOptionPane.showMessageDialog(this,"Se Registro Correctamemte");
                listarProveedor();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,e.getMessage());
        }

          else
            JOptionPane.showMessageDialog(this,"Direccion, Nombre y Telefono son obligatorios");
    }
        limpiarControles();
    
}//GEN-LAST:event_btnGrabarActionPerformed

private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed

    if(!lblIdProveedor.getText().equals("Autogenerado"))


      try {
              Proveedor objProveedor= new Proveedor();            
            
            objProveedor.setInt_id_proveedor(Integer.parseInt(lblIdProveedor.getText()));
             objProveedor.setIntl_activa(cboEstado.getSelectedIndex());
            objProveedor.setStr_nombre(txtNombre.getText());         
            objProveedor.setStr_telefono(txtTelefono.getText());
            objProveedor.setStr_fax(txtFax.getText());
            objProveedor.setStr_direccion(txtDireccion.getText());
            objProveedor.setStr_descripcion(txtDescripcion.getText());
            
            if(objProveedorCOM.actualizar(objProveedor))
            {
                JOptionPane.showMessageDialog(this,"Se Actualizo Correctamente");
                
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,e.getMessage());
        }

    else
     JOptionPane.showMessageDialog(this,"Seleccione un Departamento");
         listarProveedor();   

}//GEN-LAST:event_btnActualizarActionPerformed

