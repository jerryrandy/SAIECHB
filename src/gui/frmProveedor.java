private void jListaProveedorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jListaProveedorMouseClicked

       String criterio = jListaProveedor.getSelectedValue().toString();
       if(!criterio.toString().trim().equals(""))
       {
                
                txtNombre.setText(((Proveedor)jListaProveedor.getModel().getElementAt(jListaProveedor.getSelectedIndex())).getStr_nombre());
                txtDescripcion.setText(((Proveedor)jListaProveedor.getModel().getElementAt(jListaProveedor.getSelectedIndex())).getStr_descripcion());
                lblIdProveedor.setText(String.valueOf(((Proveedor)jListaProveedor.getModel().getElementAt(jListaProveedor.getSelectedIndex())).getInt_id_proveedor()));
                cboEstado.setSelectedIndex((((Proveedor)jListaProveedor.getModel().getElementAt(jListaProveedor.getSelectedIndex())).getIntl_activa()));
                txtTelefono.setText(((Proveedor)jListaProveedor.getModel().getElementAt(jListaProveedor.getSelectedIndex())).getStr_telefono());             
                txtFax.setText(((Proveedor)jListaProveedor.getModel().getElementAt(jListaProveedor.getSelectedIndex())).getStr_fax());
                txtDireccion.setText(((Proveedor)jListaProveedor.getModel().getElementAt(jListaProveedor.getSelectedIndex())).getStr_direccion());

       }
    
}//GEN-LAST:event_jListaProveedorMouseClicked

private void txtBuscarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBuscarKeyReleased
// TODO add your handling code here:
      modeloProveedor.clear();
        try {
            List<Proveedor> listDepartamento = objProveedorCOM.listarFiltro(txtBuscar.getText());
            if(listDepartamento!=null)
            {
                for(Proveedor p : listDepartamento)
                {
                    modeloProveedor.addElement(p);
                }
            }
           
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,e.getMessage());
        }
}//GEN-LAST:event_txtBuscarKeyReleased

