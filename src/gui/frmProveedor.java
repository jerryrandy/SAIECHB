      public void listarProveedor()
    {
        modeloProveedor.clear();
        try {
            List<Proveedor> listProveedor = objProveedorCOM.listarTodosOrdenado();
            if(listProveedor!=null)
            {
                for(Proveedor p : listProveedor)
                {
                    modeloProveedor.addElement(p);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,e.getMessage());
        }
    }
