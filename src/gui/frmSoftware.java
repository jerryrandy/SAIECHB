      public void listarTipo()
    {
        modeloSoftware.clear();
        try {
            List<Software> listSoftware = objSoftwareCOM.listarTodosOrdenado();
            if(listSoftware!=null)
            {
                for(Software m : listSoftware)
                {
                    modeloSoftware.addElement(m);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,e.getMessage());
        }
    }
