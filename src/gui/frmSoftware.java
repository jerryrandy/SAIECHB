private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
     if(!lblIdSoftware.getText().equals("Autogenerado"))
        {
            try {
            if(objSoftwareCOM.eliminar(Integer.parseInt(lblIdSoftware.getText())))
            {
                JOptionPane.showMessageDialog(this,"Se Elimino Correctamemte");
                listarTipo();
            }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this,e.getMessage());
            }            
        }
        else
            JOptionPane.showMessageDialog(this,"Seleccione una Tipo");
        limpiarControles();
        
}//GEN-LAST:event_btnEliminarActionPerformed

private void jListaTipoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jListaTipoMouseClicked

       String criterio = jListaTipo.getSelectedValue().toString();
       if(!criterio.toString().trim().equals(""))
       {
                
                txtNombre.setText(((Software)jListaTipo.getModel().getElementAt(jListaTipo.getSelectedIndex())).getStr_nombre());
                lblIdSoftware.setText(String.valueOf(((Software)jListaTipo.getModel().getElementAt(jListaTipo.getSelectedIndex())).getInt_id_software()));
                cboEstado.setSelectedIndex((((Software)jListaTipo.getModel().getElementAt(jListaTipo.getSelectedIndex())).getInt_activa()));
                cb_ListarTipo.setSelectedIndex((((Software)jListaTipo.getModel().getElementAt(jListaTipo.getSelectedIndex())).getInt_tipo()));

       }
    
}//GEN-LAST:event_jListaTipoMouseClicked

private void txtBuscarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBuscarKeyReleased
// TODO add your handling code here:
      modeloSoftware.clear();
        try {
            List<Software> listSoftware = objSoftwareCOM.listarFiltro(txtBuscar.getText());
            if(listSoftware!=null)
            {
                for(Software m : listSoftware)
                {
                    modeloSoftware.addElement(m);
                }
            }
         
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,e.getMessage());
        }
}//GEN-LAST:event_txtBuscarKeyReleased

