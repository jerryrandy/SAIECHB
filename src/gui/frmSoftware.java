private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed
       
    if(lblIdSoftware.getText().equals("Autogenerado"))
    {
     if( !txtNombre.getText().equals("") && cb_ListarTipo.getSelectedIndex()!=0)

        try {
            Software objSoftware= new Software();
            
            objSoftware.setInt_activa(cboEstado.getSelectedIndex());
            objSoftware.setInt_tipo(cb_ListarTipo.getSelectedIndex());

            objSoftware.setStr_nombre(txtNombre.getText());

             

            
            if(objSoftwareCOM.insertar(objSoftware))
            {
                JOptionPane.showMessageDialog(this,"Se Registro Correctamemte");
                listarTipo();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,e.getMessage());
            //JOptionPane.showMessageDialog(this,"Nombre Duplicado");
     }
          else
            JOptionPane.showMessageDialog(this,"Ingrese datos");
    }
        limpiarControles();
    
}//GEN-LAST:event_btnGrabarActionPerformed

private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed

    if(!lblIdSoftware.getText().equals("Autogenerado"))
      try {
                Software objSoftware= new Software();
            
            objSoftware.setInt_activa(cboEstado.getSelectedIndex());
            objSoftware.setInt_tipo(cb_ListarTipo.getSelectedIndex());

            objSoftware.setStr_nombre(txtNombre.getText());


             
            objSoftware.setInt_id_software(Integer.parseInt(lblIdSoftware.getText()));
            
            if(objSoftwareCOM.actualizar(objSoftware))
            {
                JOptionPane.showMessageDialog(this,"Se Actualizo Correctamente");
                
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,e.getMessage());
        }

    else
     JOptionPane.showMessageDialog(this,"Seleccione una Tipo");
         listarTipo();   

}//GEN-LAST:event_btnActualizarActionPerformed

