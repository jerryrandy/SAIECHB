    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
       try {

            if(modeloTablaAccesoriosNuevo!=null)
            for(Accesorios accesorios: modeloTablaAccesoriosNuevo.listar())
               {                   
                
                objAccesoriosCOM.insertarComputadorAccesorios( lblIdComputador.getText(),accesorios.getStr_id_accesorios());
                  objComponenteCOM.actualizarEstado(accesorios.getObjComponente().getStr_id_componente(),2);
                    objHistorialComponenteCOM.insertar(accesorios.getObjComponente().getStr_id_componente(),"Se Inserto en el computador "+objComputador.getStr_id_computador());    
               }
               
             if(listAccesoriosNoOperativo!=null)
            for(Accesorios accesorios:listAccesoriosNoOperativo)
            {
                objAccesoriosCOM.eliminarComputadorComponete(lblIdComputador.getText(),accesorios.getStr_id_accesorios());
                     objComponenteCOM.actualizarEstado(accesorios.getObjComponente().getStr_id_componente(),3);
                     objHistorialComponenteCOM.insertar(accesorios.getObjComponente().getStr_id_componente(),"Se Inhabilito en el computador "+objComputador.getStr_id_computador());
       
            }

                if(listAccesoriosOperativo!=null)
            for(Accesorios accesorios:listAccesoriosOperativo)
            {
                if(objAccesoriosCOM.eliminarComputadorComponete(lblIdComputador.getText(), accesorios.getStr_id_accesorios()))
                     objComponenteCOM.actualizarEstado(accesorios.getObjComponente().getStr_id_componente(),4);
                  objHistorialComponenteCOM.insertar(accesorios.getObjComponente().getStr_id_componente(),"Se Retiro en el computador "+objComputador.getStr_id_computador());
       
            }
              
                
                
               
        
                
                
                
                
                
                
             } catch (Exception ex) {
                Logger.getLogger(frmModificarAccesorios.class.getName()).log(Level.SEVERE, null, ex);
            }
            this.dispose();

    }//GEN-LAST:event_btnModificarActionPerformed

