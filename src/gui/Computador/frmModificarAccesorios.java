    private void btnQuitarCPU1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnQuitarCPU1ActionPerformed
        // TODO add your handling code here:
          if(tablaDetalleAccActual.getSelectedRow()>-1)
        {
            Accesorios objAccesorios = new Accesorios();
            
            objAccesorios = modeloTablaAccesoriosActual.listar().get(tablaDetalleAccActual.getSelectedRow());
            if(chkEstado.isSelected())
            listAccesoriosOperativo.add(objAccesorios);
            else
            listAccesoriosNoOperativo.add(objAccesorios);
            
            int c=Integer.valueOf(lblContador.getText());
            c++;
            modeloTablaAccesoriosActual.removerItemDetalle(tablaDetalleAccActual.getSelectedRow());
            modeloTablaAccesoriosActual.fireTableDataChanged();
            lblContador.setText(String.valueOf(c));
            chkEstado.setSelected(true);
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Seleccione Una Fila");
        }
    }//GEN-LAST:event_btnQuitarCPU1ActionPerformed

